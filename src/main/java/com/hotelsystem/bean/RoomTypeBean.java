package com.hotelsystem.bean;
/**
 * 房间类型表
 * @ClassName RoomTypeBean
 * @Description XXX
 * @author Ren Qiang
 * @Date 2018/8/4 上午9:38
 * @version 1.0
 */
public class RoomTypeBean {
	/**
	 *  房间类型id
	 */
	private int id;
	/**
	 *  房间类型名字
	 */
	private String name;
	/**
	 *  该房间类型的价格（未打折）
	 */
	private double price;
	/**
	 *  该房间类型的图片
	 */
	private String img;
	/**
	 *  该房间类型可以入住几人
	 */
	private int people;
	/**
	 *  该房间类型的面积
	 */
	private int area;
	/**
	 *  该房间类型的床型
	 */
	private String bedType;
	/**
	 *  该房间类型的上网方式
	 */
	private String netType;
	/**
	 *  房间的开窗方式
	 */
	private String window;
	
	// 房间有无早餐
    private String breakfirst;
    
    // 是否为无烟楼
    private String noSmoking;
    
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public int getPeople() {
		return people;
	}

	public void setPeople(int people) {
		this.people = people;
	}

	public int getArea() {
		return area;
	}

	public void setArea(int area) {
		this.area = area;
	}

	public String getBedType() {
		return bedType;
	}

	public void setBedType(String bedType) {
		this.bedType = bedType;
	}

	public String getNetType() {
		return netType;
	}

	public void setNetType(String netType) {
		this.netType = netType;
	}

	public String getWindow() {
		return window;
	}

	public void setWindow(String window) {
		this.window = window;
	}

	

	public String getBreakfirst() {
		return breakfirst;
	}

	public void setBreakfirst(String breakfirst) {
		this.breakfirst = breakfirst;
	}

	public String getNoSmoking() {
		return noSmoking;
	}

	public void setNoSmoking(String noSmoking) {
		this.noSmoking = noSmoking;
	}

	public RoomTypeBean() {
		super();
		// TODO Auto-generated constructor stub
	}

	@Override
	public String toString() {
		return "RoomTypeBean [id=" + id + ", name=" + name + ", price=" + price
				+ ", img=" + img + ", people=" + people + ", area=" + area
				+ ", bedType=" + bedType + ", netType=" + netType + ", window="
				+ window + ", breakfirst=" + breakfirst + ", noSmoking="
				+ noSmoking + "]";
	}

	

}
