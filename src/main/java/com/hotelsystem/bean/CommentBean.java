package com.hotelsystem.bean;

import java.util.Date;

/**
 * 评论表
 * @author 张加林
 *
 */

public class CommentBean {
	/**
	 * 评论ID
	 */
	private int id;
	/**
	 * 评论日期
	 */
	private Date date;
	/**
	 * 评论内容
	 */
	private String content;
	/**
	 * 评论人信息
	 */
	private UserLoginBean userLogin;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public UserLoginBean getUserLogin() {
		return userLogin;
	}
	public void setUserLogin(UserLoginBean userLogin) {
		this.userLogin = userLogin;
	}
	@Override
	public String toString() {
		return "CommentBean [id=" + id + ", date=" + date + ", content=" + content + ", userLogin=" + userLogin + "]";
	}
	
}
