package com.hotelsystem.bean;

import java.util.Date;

/**
 * @ClassName ConsumeBean
 * @Description TODO
 * @Author Wu Yimin
 * @Date 2018/8/7 上午11:44
 * @Version 1.0
 **/
public class ConsumeBean {
    private String conId;
    private String conCheckId;
    private String conName;
    private Integer conMount;
    private Double conMoney;
    private Date conDate;
    private Integer conFlag;
    private String conAlipayId;

    public String getConAlipayId() {
        return conAlipayId;
    }

    public void setConAlipayId(String conAlipayId) {
        this.conAlipayId = conAlipayId;
    }

    public ConsumeBean() {
    }

    public ConsumeBean(String conId, String conCheckId, String conName, Integer conMount, Double conMoney, Date conDate, Integer conFlag, String conAlipayId) {
        this.conId = conId;
        this.conCheckId = conCheckId;
        this.conName = conName;
        this.conMount = conMount;
        this.conMoney = conMoney;
        this.conDate = conDate;
        this.conFlag = conFlag;
        this.conAlipayId = conAlipayId;
    }

    public String getConId() {
        return conId;
    }

    public void setConId(String conId) {
        this.conId = conId;
    }

    public String getConCheckId() {
        return conCheckId;
    }

    public void setConCheckId(String conCheckId) {
        this.conCheckId = conCheckId;
    }

    public String getConName() {
        return conName;
    }

    public void setConName(String conName) {
        this.conName = conName;
    }

    public Integer getConMount() {
        return conMount;
    }

    public void setConMount(Integer conMount) {
        this.conMount = conMount;
    }

    public Double getConMoney() {
        return conMoney;
    }

    public void setConMoney(Double conMoney) {
        this.conMoney = conMoney;
    }

    public Date getConDate() {
        return conDate;
    }

    public void setConDate(Date conDate) {
        this.conDate = conDate;
    }

    public Integer getConFlag() {
        return conFlag;
    }

    public void setConFlag(Integer conFlag) {
        this.conFlag = conFlag;
    }

    @Override
    public String toString() {
        return "ConsumeBean{" +
                "conId='" + conId + '\'' +
                ", conCheckId='" + conCheckId + '\'' +
                ", conName='" + conName + '\'' +
                ", conMount=" + conMount +
                ", conMoney=" + conMoney +
                ", conDate=" + conDate +
                ", conFlag=" + conFlag +
                '}';
    }
}
